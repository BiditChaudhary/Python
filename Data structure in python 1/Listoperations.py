#List operations
My_list = ["Mango","Banana","Apple"]
print(My_list)
My_list.append("Guava")
print(My_list)
My_list.remove("Banana")
print(My_list)
My_list.pop(1)
print(My_list)
print(len(My_list))
My_list[1] = "Watermelon"
print(My_list)
My_list.append("Grapes")
My_list.append("Orange")
My_list.sort()
print(My_list)
My_list.reverse()
print(My_list)
print(My_list[-1:-3:-1])